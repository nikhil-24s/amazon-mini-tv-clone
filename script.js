function scrollRight(){
    document.getElementById('thumbnail').scrollLeft += 700;
    document.getElementById('left-btn').style.display= 'inline';
    
}
function scrollLeft1(){
    document.getElementById('thumbnail').scrollLeft -= 700;
    if(document.getElementById('thumbnail').scrollLeft <= 700)
    {
        document.getElementById('left-btn').style.display= 'none';
    }
}

